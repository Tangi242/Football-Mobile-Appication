// Helper function to get image by category and index
export const getOnlineImage = (category, index = 0) => {
  const collection = onlineImages[category];
  if (!collection) return onlineImages.matchBanners[0];
  if (Array.isArray(collection)) {
    return collection[index % collection.length];
  }
  return collection;
};

// Helper function to get team logo/flag (deprecated - use teamAssets.js)
// Kept for backward compatibility
export const getTeamLogo = (teamName) => {
  if (!teamName) return onlineImages.logos.namibia;
  const name = teamName.toLowerCase();
  if (name.includes('namibia') || name.includes('brave') || name.includes('footballhub')) return onlineImages.logos.namibia;
  if (name.includes('poland')) return onlineImages.logos.poland;
  if (name.includes('netherlands')) return onlineImages.logos.netherlands;
  if (name.includes('angola')) return onlineImages.logos.angola;
  return onlineImages.logos.namibia; // Default to Namibia
};
